import type { Doc } from "@repo/backend/convex/_generated/dataModel";
import type { MutationCtx } from "@repo/backend/convex/_generated/server";
import { CONTENT_SYNC_BATCH_LIMITS } from "@repo/backend/convex/contentSync/constants";
import { assertContentSyncBatchSize } from "@repo/backend/convex/contentSync/lib/errors";
import { syncContentSearch } from "@repo/backend/convex/contents/helpers/search/write";
import { internalMutation } from "@repo/backend/convex/functions";
import { localeValidator } from "@repo/backend/convex/lib/validators/contents";
import { ConvexError, type Infer, v } from "convex/values";

const syncedQuranSearchDocumentValidator = v.object({
  contentHash: v.string(),
  description: v.string(),
  locale: localeValidator,
  route: v.string(),
  text: v.string(),
  title: v.string(),
});

const syncSummaryValidator = v.object({
  created: v.number(),
  unchanged: v.number(),
  updated: v.number(),
});

type SyncedQuranSearchDocument = Infer<
  typeof syncedQuranSearchDocumentValidator
>;

/** Loads the persisted Quran route graph projection required by search sync. */
async function loadQuranRouteProjection(
  ctx: MutationCtx,
  document: Pick<SyncedQuranSearchDocument, "locale" | "route">
) {
  const route = await ctx.db
    .query("contentRoutes")
    .withIndex("by_locale_and_route", (q) =>
      q.eq("locale", document.locale).eq("route", document.route)
    )
    .unique();

  if (
    route?.kind === "quran-surah" &&
    route.section === "quran" &&
    route.content_id === route.assetId
  ) {
    return route;
  }

  throw new ConvexError({
    code: "QURAN_SEARCH_ROUTE_PROJECTION_MISSING",
    message: `Quran search sync requires a persisted route graph projection for ${document.locale}/${document.route}.`,
  });
}

/** Removes stale search rows for the same Quran route with non-catalog IDs. */
async function deleteStaleQuranSearchRows(
  ctx: MutationCtx,
  route: Doc<"contentRoutes">
) {
  const existingRows = await ctx.db
    .query("contentSearch")
    .withIndex("by_locale_and_route", (q) =>
      q.eq("locale", route.locale).eq("route", route.route)
    )
    .take(6);

  if (existingRows.length > 5) {
    throw new ConvexError({
      code: "QURAN_SEARCH_ROUTE_DUPLICATE_LIMIT_EXCEEDED",
      message: `Quran search route ${route.locale}/${route.route} has too many duplicate search rows.`,
    });
  }

  for (const row of existingRows) {
    if (row.content_id !== route.content_id) {
      await ctx.db.delete(row._id);
    }
  }
}

/** Upserts Quran documents generated by the filesystem sync script. */
export const bulkSyncQuranSearch = internalMutation({
  args: {
    documents: v.array(syncedQuranSearchDocumentValidator),
  },
  returns: syncSummaryValidator,
  handler: async (ctx, args) => {
    assertContentSyncBatchSize({
      functionName: "bulkSyncQuranSearch",
      limit: CONTENT_SYNC_BATCH_LIMITS.quranSearchDocuments,
      received: args.documents.length,
      unit: "Quran search documents",
    });

    const now = Date.now();
    let created = 0;
    let unchanged = 0;
    let updated = 0;

    for (const document of args.documents) {
      const route = await loadQuranRouteProjection(ctx, document);
      await deleteStaleQuranSearchRows(ctx, route);

      const result = await syncContentSearch(ctx, {
        alignmentId: route.alignmentId,
        assetId: route.assetId,
        conceptId: route.conceptId,
        ...document,
        learningObjectId: route.learningObjectId,
        lensId: route.lensId,
        section: "quran",
        syncedAt: now,
      });

      if (result === "created") {
        created++;
        continue;
      }

      if (result === "updated") {
        updated++;
        continue;
      }

      unchanged++;
    }

    return { created, unchanged, updated };
  },
});
