import { CONTENT_SYNC_BATCH_LIMITS } from "@repo/backend/convex/contentSync/constants";
import { assertContentSyncBatchSize } from "@repo/backend/convex/contentSync/lib/errors";
import { syncContentSearch } from "@repo/backend/convex/contents/search/write";
import { internalMutation } from "@repo/backend/convex/functions";
import { localeValidator } from "@repo/backend/convex/lib/validators/contents";
import { v } from "convex/values";

const syncedQuranSearchDocumentValidator = v.object({
  contentHash: v.string(),
  description: v.string(),
  locale: localeValidator,
  route: v.string(),
  text: v.string(),
  title: v.string(),
});

const syncSummaryValidator = v.object({
  created: v.number(),
  unchanged: v.number(),
  updated: v.number(),
});

/** Upserts Quran documents generated by the filesystem sync script. */
export const bulkSyncQuranSearch = internalMutation({
  args: {
    documents: v.array(syncedQuranSearchDocumentValidator),
  },
  returns: syncSummaryValidator,
  handler: async (ctx, args) => {
    assertContentSyncBatchSize({
      functionName: "bulkSyncQuranSearch",
      limit: CONTENT_SYNC_BATCH_LIMITS.quranSearchDocuments,
      received: args.documents.length,
      unit: "Quran search documents",
    });

    const now = Date.now();
    let created = 0;
    let unchanged = 0;
    let updated = 0;

    for (const document of args.documents) {
      const result = await syncContentSearch(ctx, {
        ...document,
        section: "quran",
        syncedAt: now,
      });

      if (result === "created") {
        created++;
        continue;
      }

      if (result === "updated") {
        updated++;
        continue;
      }

      unchanged++;
    }

    return { created, unchanged, updated };
  },
});
